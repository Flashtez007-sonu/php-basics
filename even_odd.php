//Even Odd Program
Logic:
-Take a number.
-Divide it by 2.
-If the remainder is 0, print number is even.

<?php 
    $number=1231236;
    if($number%2==0)
    {
        echo "$number is even Number";
    }
    else{
        echo "$number is odd Number";
    }

?>

output:
1231236 is even Number
